package days03;

public class Variable06 {
	// 정수형 byte(1), short(2), int(4)
	// 실수형 float(4), double(8)
	// 문자형 char(2)
	// 진위형 boolean(1)
	// 문자열 String(class, no determined size)
	public static void main(String[] args) {
		// Declare Variable
		// 1. Declare and assign
		int n1;
		n1 = 10;
		// 2. Declare and assign at the same time
		int n2 = 10;
		// 3. Declare multiple variables at once
		int n3, n4, n5;
		// 4. Declare and assign multiple variables at once
		int n6 = 10, n7 = 20;
		// 5. You can opt out of assign certain variables
		int n8, n9 = 90, n10;
	}
}
